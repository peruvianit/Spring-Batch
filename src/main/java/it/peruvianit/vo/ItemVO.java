package it.peruvianit.vo;

public class ItemVO {
	private Integer progressivoItem;
	private String descrizioneItem;

	public ItemVO(){}

	
	public Integer getProgressivoItem() {
		return progressivoItem;
	}

	public void setProgressivoItem(Integer progressivoItem) {
		this.progressivoItem = progressivoItem;
	}

	public String getDescrizioneItem() {
		return descrizioneItem;
	}

	public void setDescrizioneItem(String descrizioneItem) {
		this.descrizioneItem = descrizioneItem;
	}
}
